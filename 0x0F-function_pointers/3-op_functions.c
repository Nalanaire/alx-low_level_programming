#include "3-calc.h"

/**
 * op_add - calculate the sum of the two intergers
 * @a: firts interger
 * @b: second interger
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate the difference of the two intergers
 * @a: firts interger
 * @b: second interger
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculate the product of the two intergers
 * @a: firts interger
 * @b: second interger
 * Return: difference of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculate the divisio of the two intergers
 * @a: firts interger
 * @b: second interger
 * Return: difference of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate the remainder of the two intergers
 * @a: firts interger
 * @b: second interger
 * Return: difference of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
